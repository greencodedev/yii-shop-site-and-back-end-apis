    public function isBase64Encoded()
    {
        $files = $this->getFormFiles();
        $flag = ($files !=null);
        $this->base64 = $flag;
        return $flag; 
        

    }

    private function notNull($data){
        if($data!="" && $data!=" "){
            return true;
        }

        return false;
    }

    public function setBase64Files(){
       
            $files = $this->getFormFiles();
            $names = $this->getFormFileNames();
            $mimes = $this->getFormFileMimes();
            
            if(is_array($files)){
                $respFiles = [];
                foreach($files as $key=>$file){
                    if($this->notNull($file)){
                        $respFiles[] = new Base64EncodedFile(
                                                $file,
                                            isset($names[$key])?$names[$key]:null,
                                            isset($mimes[$key])?$mimes[$key]:null);
                    }                   
                }
                return $respFiles;

            }else{
                if($this->notNull($files)){
                        return new Base64EncodedFile(
                                        $files,
                                    isset($names[0])?$names[0]:null,
                                    isset($mimes[0])?$mimes[0]:null);
                }else{
                        return null;
                }
            }


    }


    public function getFormFiles(){
        return \Yii::$app
                        ->getRequest()
                        ->post()[$this->getClassName()]['files'];
    }

    public function getFormFileNames(){
        return \Yii::$app
                        ->getRequest()
                        ->post()[$this->getClassName()]['names'];

    }

    public function getFormFileMimes(){
        return \Yii::$app
                        ->getRequest()
                        ->post()[$this->getClassName()]['mimes'];

    }

    

    

    public function getClassName(){
        $val = explode('\\',get_class($this));
        return ($val[(count($val)-1)]);
    }

}


class Base64EncodedFile{

    public $data;
    public $name;
    public $mime;
    public $extension;

    public function __construct($file, $name = null, $mime = null)
    {
        $this->data = $file;
        $this->name = $name;
        $this->mime = $mime;
        $this->setFileExtension();
    }

    public function __toString()
    {
        return $this->name;
