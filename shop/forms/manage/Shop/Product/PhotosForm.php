    public function saveAs($path){
        return file_put_contents($path,
        $this->decodedBase64(
            $this->filterContent()));
    }

    private function decodedBase64($data){
        return base64_decode($data);
    }

    private function filterContent(){
        $arr = explode(";base64,", $this->data);
        return $arr[(count($arr)-1)] ;
    }
  
    private function setFileExtension(){

        if($this->name!=null && $this->name!=''){
            $nameArr = explode('.',$this->name);
            $this->extension = $nameArr[(count($nameArr)-1)];
            return true;
        }

        $mime = $this->getB64Type();
        if($mime==null){
            $mime = $this->getFileType();
        }
       $this->extension = $this->mime2ext($mime);
    }
    
    
    private function getFileType(){

        $f = finfo_open();

        $mime_type = finfo_buffer($f, $this->decodedBase64($this->data), FILEINFO_MIME_TYPE);      
        return $mime_type;                     
    }

